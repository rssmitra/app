<div style="text-align: center;"><b>HASIL PEMERIKSAAN DIAGNOSTIK NON INVASIF VASKULER</b></div>
<br><b>Pada pemeriksaan duplex sonography femoralis didapatkan :</b>
<br>
<br>
<b>Arteri</b> :
<br>-	Gambaran anatomi pembuluh darah arteri femoralis communis, arteri poplitea, arteri tibialis anterior-posterior, arteri dorsalis pedis bilateral rata dan tidak menebal
<br>-	Arteri femoralis communis, arteri poplitea, arteri tibialis anterior-posterior, arteri dorsalis pedis bilateral morfologi kurva Doppler ..............
<br>-	Colour coded mengisi penuh lumen pembuluh darah arteri femoralis communis, arteri poplitea, arteri tibialis anterior-posterior, arteri dorsalis pedis bilateral
<br>
<br>
<b>Vena</b> :
<br>-	Compressi Ultrasound (CUS) negative pada vena femoralis, vena poplitea bilateral
<br>-	Augmentasi (+) dengan uji squeeze distal pada vena femoralis, vena poplitea bilateral
<br>-	Reflux (-) pada kedua tungkai 
<br>-	Diameter GSV sinistra proximal mm, mid mm, distal mm
<br>-	Diameter GSV dextra proximal mm, mid mm, distal mm
<br>
<br>
<b>Kesimpulan</b> :
<br>-	Normal flow arteri pada kedua tungkai
<br>-	CVI (-) pada kedua tungkai
<br>-	DVT (-) pada kedua tungkai
