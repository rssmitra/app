<div style="text-align: center;"><b>HASIL PEMERIKSAAN DIAGNOSTIK NON INVASIF VASKULER</b></div>
<br>
<br>
<b>Pada pemeriksaan duplex ekstremitas atas didapatkan :</b>
<br>
<br>
<b>Arteri</b> :
<br>-	Gambaran anatomi pembuluh darah arteri subclavia, arteri axilaris, arteri brachialis, arteri radialis, arteri ulnaris bilateral rata dan tidak menebal
<br>-	Arteri subclavia, arteri axilaris, arteri brachialis, arteri radialis, arteri ulnaris bilateral morfologi kurva Doppler triphasic
<br>
<br>
<b>Vena</b> :
<br>-	Compressi Ultrasound (CUS) negatif pada vena brachialis bilateral
<br>-	Augmentasi (+) dengan uji squeeze distal pada vena brachialis bilateral
<br>
<br>
<b>Kesimpulan</b> :
<br>-	Normal flow arteri dan vena pada kedua ekstremitas atas
<br>-	Tidak ditemukan thrombosis (DVT) pada vena dalam di kedua ekstremitas atas
<br>