    Cara menggunakan skor FLACC :
    <ul>
        <li>Pasien yang sadar/bangun: pengamatan pasien dilakukan selama 1-5 menit atau lebih. Bagian yang diamati 
            adalah tungkai dan tubuh yang terbuka/terlihat, perubahan posisi dan aktivitas, kemudian pemeriksa merabah tubuh 
            pasien untuk menilai ketegangan otot dan melakukan intervensi atau konseling jika diperlukan.</li>
        <li>Bila pasien tidur; pengamatan dilakukan selama 5 menit atau lebih. Bagian yang diamati adalah tubuh dan tungkai
            yang terbuka/terlihat. Jika memungkinkan rubah posisi pasien, raba tubuh pasien, serta nilai tegangan otot.</li>
    </ul>
