<p style="text-align: justify">
    SKALA NYERI RINGAN : 1-3
    <ol>
        <li>Edukasi pasien dan keluarga.</li>
        <li>Ajarkan tehknik penggunaan non farmakologi misalnya : relaksi, biofeedback, hypnosis, guide imagery,
            terapi musik, distraksi, terapi bermain, acupressure, terapi dingin/panas maupun terapi pijitan.</li>
        <li>Kaji kembali nyeri setelah 1 jam jika tindakan tehnik non farmakologi tidak berhasil kolaborasi dengan dokter 
            dalam pemberian terapi farmakologi (terapi NSAID).</li>
        <li>Kaji nyeri setelah 8 jam dari pemberian terapi farmakologi.</li>
    </ol>
</p>
